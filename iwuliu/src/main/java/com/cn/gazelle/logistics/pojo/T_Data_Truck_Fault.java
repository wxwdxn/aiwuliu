package com.cn.gazelle.logistics.pojo;

import java.util.Date;

/**
 * 类 名 称：T_Data_Truck_Fault
 * 内容描述：卡车故障信息表实体类
 *@authot QJ
 */
public class T_Data_Truck_Fault {
    private String fault_number;// 故障No
    private String obd_id;// OBD ID
    private Date client_acquisition_time;// 接收时间
    private String fault_code;// 故障码
    private String fault_attribute;// 故障属性
    private String fault_happen_times;// 发生次数
    private int delete_flag;// 删除标识符
    private Date last_update;// 最终更新日
    private String last_update_user_id;// 最终更新者ID

    public String getFault_number() {
        return fault_number;
    }

    public void setFault_number(String fault_number) {
        this.fault_number = fault_number;
    }

    public String getObd_id() {
        return obd_id;
    }

    public void setObd_id(String obd_id) {
        this.obd_id = obd_id;
    }

    public Date getClient_acquisition_time() {
        return client_acquisition_time;
    }

    public void setClient_acquisition_time(Date client_acquisition_time) {
        this.client_acquisition_time = client_acquisition_time;
    }

    public String getFault_code() {
        return fault_code;
    }

    public void setFault_code(String fault_code) {
        this.fault_code = fault_code;
    }

    public String getFault_attribute() {
        return fault_attribute;
    }

    public void setFault_attribute(String fault_attribute) {
        this.fault_attribute = fault_attribute;
    }

    public String getFault_happen_times() {
        return fault_happen_times;
    }

    public void setFault_happen_times(String fault_happen_times) {
        this.fault_happen_times = fault_happen_times;
    }

    public int getDelete_flag() {
        return delete_flag;
    }

    public void setDelete_flag(int delete_flag) {
        this.delete_flag = delete_flag;
    }

    public Date getLast_update() {
        return last_update;
    }

    public void setLast_update(Date last_update) {
        this.last_update = last_update;
    }

    public String getLast_update_user_id() {
        return last_update_user_id;
    }

    public void setLast_update_user_id(String last_update_user_id) {
        this.last_update_user_id = last_update_user_id;
    }
}
package com.cn.gazelle.logistics.pojo;

import java.util.Date;

/**
 * 类 名 称：T_Master_Sub_Line_Detail
 * 内容摘要：干线路线节点基础信息实体类
 * @author QJ
 */
public class T_Master_Sub_Line_Detail {
    private String line_id;// ID
    private String main_line_info_id;// 干线路线ID
    private String node_no;// 节点编号
    private String node_name;// 节点名称
    private String longitude;// 节点经度
    private String latitude;// 节点纬度
    private int delete_flag;// 删除标识符
    private Date last_update;// 最终更新日
    private String last_update_user_id;// 最终更新者ID

    public String getLine_id() {
        return line_id;
    }

    public void setLine_id(String line_id) {
        this.line_id = line_id;
    }

    public String getMain_line_info_id() {
        return main_line_info_id;
    }

    public void setMain_line_info_id(String main_line_info_id) {
        this.main_line_info_id = main_line_info_id;
    }

    public String getNode_no() {
        return node_no;
    }

    public void setNode_no(String node_no) {
        this.node_no = node_no;
    }

    public String getNode_name() {
        return node_name;
    }

    public void setNode_name(String node_name) {
        this.node_name = node_name;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public int getDelete_flag() {
        return delete_flag;
    }

    public void setDelete_flag(int delete_flag) {
        this.delete_flag = delete_flag;
    }

    public Date getLast_update() {
        return last_update;
    }

    public void setLast_update(Date last_update) {
        this.last_update = last_update;
    }

    public String getLast_update_user_id() {
        return last_update_user_id;
    }

    public void setLast_update_user_id(String last_update_user_id) {
        this.last_update_user_id = last_update_user_id;
    }
}
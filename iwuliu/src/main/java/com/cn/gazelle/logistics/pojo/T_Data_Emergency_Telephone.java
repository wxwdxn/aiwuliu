package com.cn.gazelle.logistics.pojo;

import java.util.Date;

/**
 * 类 名 称：T_Data_Emergency_Telephone
 * 内容描述：紧急电话信息实体类
 *@authot YK
 */
public class T_Data_Emergency_Telephone {

    private String emergency_telephone_id;                      // 记录id
    private String emergency_telephone_type;                    // 公司名称
    private String province_id;                                 // 营业执照号码
    private String city_id;                                     // 营业执照照片
    private String telephone;                                   // 公司固定电话
    private String delete_flag;                                 // 联系人
    private Date last_update;                                 // 性别
    private String last_update_user_id;                         // 联系人移动电话

    public String getEmergency_telephone_id() {
        return emergency_telephone_id;
    }

    public void setEmergency_telephone_id(String emergency_telephone_id) {
        this.emergency_telephone_id = emergency_telephone_id;
    }

    public String getEmergency_telephone_type() {
        return emergency_telephone_type;
    }

    public void setEmergency_telephone_type(String emergency_telephone_type) {
        this.emergency_telephone_type = emergency_telephone_type;
    }

    public String getProvince_id() {
        return province_id;
    }

    public void setProvince_id(String province_id) {
        this.province_id = province_id;
    }

    public String getCity_id() {
        return city_id;
    }

    public void setCity_id(String city_id) {
        this.city_id = city_id;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getDelete_flag() {
        return delete_flag;
    }

    public void setDelete_flag(String delete_flag) {
        this.delete_flag = delete_flag;
    }

    public Date getLast_update() {
        return last_update;
    }

    public void setLast_update(Date last_update) {
        this.last_update = last_update;
    }

    public String getLast_update_user_id() {
        return last_update_user_id;
    }

    public void setLast_update_user_id(String last_update_user_id) {
        this.last_update_user_id = last_update_user_id;
    }
}

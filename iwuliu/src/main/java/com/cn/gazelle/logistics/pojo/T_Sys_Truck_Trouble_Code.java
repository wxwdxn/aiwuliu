package com.cn.gazelle.logistics.pojo;

import java.util.Date;

/**
 * 类 名 称：T_Sys_Truck_Trouble_Code
 * 内容描述：乘用车故障码实体类
 *@authot YK
 */
public class T_Sys_Truck_Trouble_Code {

    private String code_id;                               // 故障码id
    private String trouble_id;                            // PID号
    private String trouble_code;                          // 故障码
    private String trouble_content;                       // 故障码内容
    private String truck_type;                            // 车辆使用类型
    private Date trouble_date;                            // 添加时间
    private String delete_states;                         // 删除状态
    private String trouble_remark;                        // 备注

    public String getCode_id() {
        return code_id;
    }

    public void setCode_id(String code_id) {
        this.code_id = code_id;
    }

    public String getTrouble_id() {
        return trouble_id;
    }

    public void setTrouble_id(String trouble_id) {
        this.trouble_id = trouble_id;
    }

    public String getTrouble_code() {
        return trouble_code;
    }

    public void setTrouble_code(String trouble_code) {
        this.trouble_code = trouble_code;
    }

    public String getTrouble_content() {
        return trouble_content;
    }

    public void setTrouble_content(String trouble_content) {
        this.trouble_content = trouble_content;
    }

    public String getTruck_type() {
        return truck_type;
    }

    public void setTruck_type(String truck_type) {
        this.truck_type = truck_type;
    }

    public Date getTrouble_date() {
        return trouble_date;
    }

    public void setTrouble_date(Date trouble_date) {
        this.trouble_date = trouble_date;
    }

    public String getDelete_states() {
        return delete_states;
    }

    public void setDelete_states(String delete_states) {
        this.delete_states = delete_states;
    }

    public String getTrouble_remark() {
        return trouble_remark;
    }

    public void setTrouble_remark(String trouble_remark) {
        this.trouble_remark = trouble_remark;
    }
}
